package com.game.evolution.domain;

public class Card {
	// 牌值
	private int value;
	// 牌型
	private String type;

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}
